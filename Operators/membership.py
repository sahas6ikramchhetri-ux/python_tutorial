# 1. in -> check if value exists in sequence (such as list, string, tuple)

# syntax: value in sequence

fruits = ['apple','mango','orange']

print(f"Check: {'mango' in fruits}")
print(f"Check: {'banana' in fruits}")


email = 'suresh@gmail.com'
print(f"Check @: {'@' in email}")

# 2. not in
print(f"Check: {'mango' not in fruits}")