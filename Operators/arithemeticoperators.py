# Arthmetic operators -> used to perfrom mathmaetical operations

num1 = 10
num2 = 20

# 1. addition
addition = num1 + num2
print(f"The sum of {num1} and {num2} is {addition}.")

# 2. subtraction
subtraction = num2 - num1
print(f"The difeerence of {num1} and {num2} is {subtraction}.")

# 3. multiplication
multiply = num2 * num1
print(f"The multiplicion of {num1} and {num2} is {multiply}.")

# 4. Divide
divide = num2 / num1
print(f"The division of {num1} and {num2} is {divide}.")

# 5. modulus
modulus = num1 % 2
print(f"Result: {modulus}")

# 6. power
power = num1 ** 3
print(f"Result2: {power}")

# 7. floor division -> always round down
result = 11 // 3
print(f"Result3: {result}")