x = 10
y = 20
z = 10
a = "10"

print(f"Greater Than: {x > y}")
print(f"Less Than: {x < y}")

print(f"Greater Than and equal to: {x >= z}")
print(f"Less Than and equal to: {x <= z}")

# equal to -> value and data type must be same
print(f"Equal To: {x == y}")
print(f"Equal To 1: {x == z}")
print(f"Equal To 2: {x == a}")

# not equal to 
print(f"Not equal to: {x != y}")
print(f"Not equal to 1: {x != z}")
print(f"Not equal to 2: {x != a}")